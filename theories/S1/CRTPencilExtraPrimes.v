   re-verification chases the 500 cons cells whenever a proof term
   mentions `crt_primes_pencil_extra`, causing >10-min timeouts.

   Keep `crt_primes_pencil` transparent so `unfold` works in proofs
   that need the `... ++ ...` form. *)
