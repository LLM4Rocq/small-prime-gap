(* ==================================================================
   CRTPencilExtraPrimes.v

   500 additional 31-bit primes appended to crt_primes_all to give
   the pencil-side CRT product enough headroom (~32500 bits, vs
   the 21300-bit product of the original 710 primes) to bound
   2 * (fl_coeff_bound 42 (max_abs_entry pencil_mat_int) +
        |D_pencil_int_value|).

   The main-branch lift (CRTLift.fl_eq_flint, charpoly_Z_A) is
   unaffected — its bounds use the smaller 710-prime product.
   ================================================================== *)

