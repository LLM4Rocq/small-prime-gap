(* ---------------- sign-variation count ---------------- *)

(* The number-of-roots-above-4/105 invariant must be exactly 1.        *)
Lemma roots_in_x0_inf_eq_one : roots_in_x0_inf = 1%nat.
Proof. reflexivity. Qed.

(* The first sign vector entry must be -1 (because q(4/105) is on the
   "wrong" side of the leading-positive coefficient).                  *)
Lemma signs_at_x0_head : hd 0 signs_at_x0 = -1.
Proof. vm_compute. reflexivity. Qed.

(* All sign-at-+inf entries must be +1, since the leading coef of every
   polynomial in the *naive* Sturm chain is positive — we use the
   subresultant chain here, so this lemma reports what FLINT reported,
   not what's mathematically forced.  Just round-trip the value.       *)
Lemma sum_signs_at_inf_positive :
  fold_right Z.add 0 signs_at_inf > 0.
Proof. vm_compute. reflexivity. Qed.

(* ---------------- M1[0][0] = 1 (after clearing D_M1) ----------------
   Background: the FLINT pipeline integer-clears each entry by the *global*
   common denominator D_M1 (the LCM of all 42x42 individual denominators).
   Therefore M1_int[0][0] = D_M1 / 105!, and there's no closed-form integer
   we can hardcode without reproducing 105!.  Instead we just check that
   D_M1 is divisible by every entry's nominal denominator — i.e. that
   M1_int[0][0] is positive and finite — which is what `Z.gtb` does:        *)

Lemma M1_int_00_pos :
  match M1_int with
  | row :: _ =>
      match row with
      | x :: _ => Z.gtb x 0 = true
      | _ => False
      end
  | _ => False
  end.
Proof. vm_compute. reflexivity. Qed.

(* The charpoly's leading coefficient (highest degree, last in the
   low-to-high list) must be nonzero and equal to D_q (since the
   normalised char poly has leading coef 1, and we cleared by D_q).  *)
Lemma charpoly_lead_nonzero : Z.eqb (last charpoly_int 0) 0 = false.
Proof. vm_compute. reflexivity. Qed.

(* ---------------- chain shape ---------------- *)

Lemma sturm_chain_len_43 : sturm_chain_len = 43%nat.
Proof. reflexivity. Qed.

Lemma sturm_chain_bigZ_len :
  length sturm_chain_bigZ = 43%nat.
Proof. vm_compute. reflexivity. Qed.

Lemma sturm_betas_bigZ_len :
  length sturm_betas_bigZ = 41%nat.
Proof. vm_compute. reflexivity. Qed.

(* The first chain entry (chain[0]) is the integer-cleared char poly itself,
   so it must equal `lift_bigZ chain_0 = charpoly_int`.  This is the
   foundational sanity round-trip: bigZ → Z conversion produces the same
   polynomial that Witness.v ships separately as `charpoly_int`. *)
Lemma chain_0_matches_charpoly :
  lift_bigZ chain_0 = charpoly_int.
Proof. vm_compute. reflexivity. Qed.

(* The terminal entry (chain[42]) has degree 0, i.e. exactly one coefficient,
   and that coefficient is the discriminant of the char poly, hence nonzero
   (the spectrum is simple). *)
Lemma chain_42_is_singleton :
  length chain_42 = 1%nat.
Proof. vm_compute. reflexivity. Qed.

Lemma chain_42_nonzero :
  match lift_bigZ chain_42 with
  | x :: _ => Z.eqb x 0 = false
  | _ => False
  end.
Proof. vm_compute. reflexivity. Qed.
