(* ============================================================== *)
(*  Smoke tests for the autogenerated Maynard certificate.         *)
(*  These confirm that the Witness.v data round-trips correctly    *)
(*  through the FLINT pipeline → JSON → Rocq:                       *)
(*    - the matrices have the right shape (42x42)                   *)
(*    - the charpoly has the right degree (42, i.e. 43 coefs)       *)
(*    - the sign vectors have the right length                      *)
(*    - a couple of specific entries match closed-form integrals    *)
(*    - the precomputed sign-variation count is exactly 1.          *)
(*  None of these tests is part of the eventual Sturm proof; they   *)
(*  exist to catch JSON-emitter / typo bugs early.                  *)
(* ============================================================== *)

