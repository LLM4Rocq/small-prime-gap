Proof. vm_compute. reflexivity. Qed.

Lemma length_M1_int_rows :
  forallb (fun row => Nat.eqb (length row) 42) M1_int = true.
Proof. vm_compute. reflexivity. Qed.

Lemma length_M2_int_rows :
